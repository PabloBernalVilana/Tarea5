/* @Author Pablo Bernal Vilana
 * Muestra por pantalla los numeros del 1 al 100
 * mediante un bucle FOR
 * 1 y 100 Ambos incluidos
 */

public class MainApp {

	public static void main(String[] args) {
		// declaracion de la variable y asignacion de un valor inicial
		// hasta que la variable numero1 valga 100 o sea mayor que 100
		// incrementara en 1 y mostrara por pantall el valor de numero
		for (int numero1 = 0 ; numero1 <= 100 ; numero1++) {
			System.out.println("numero = " + numero1);
		}
	}
}
